package main

import "fmt"

func main() {
	var mes int = 4

	switch mes {
	case 1:
		fmt.Printf("\nEl mes es enero\n")
	case 2:
		fmt.Printf("\nEl mes es febrero\n")
	case 3:
		fmt.Printf("\nEl mes es marzo\n")
	case 4:
		fmt.Printf("\nEl mes es abril\n")
	case 5:
		fmt.Printf("\nEl mes es mayo\n")
	case 6:
		fmt.Printf("\nEl mes es junio\n")
	case 7:
		fmt.Printf("\nEl mes es julio\n")
	case 8:
		fmt.Printf("\nEl mes es agosto\n")
	case 9:
		fmt.Printf("\nEl mes es septiembre\n")
	case 10:
		fmt.Printf("\nEl mes es octubre\n")
	case 11:
		fmt.Printf("\nEl mes es noviembre\n")
	case 12:
		fmt.Printf("\nEl mes es diciembre\n")
	}
}
