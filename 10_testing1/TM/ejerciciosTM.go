//* Ejercicio 1
//? ¿Cuáles son las diferencias entre White Box y Black Box?
/*
Black Box: es una estructura de testing donde el funcionamiento interno es desconocido
           por quien lo prueba. El objetivo es evaluar el comportamiento de un componente
 		   en distintos escenarios.
White Box: Quien ejecuta las pruebas, ve y conoce el codigo. Su diseño esta ligado al cod fuente.
		   Este procedimiento se aplica para poder detectar vulnerabilidades en el codigo.
		   Pueden aplicarse en Test Unitarios y de Integracion.


*/

//* Ejercicio 2
// ?¿Qué es un test funcional?.
/*
	Son pruebas basadas en la entrada y salida del software, con el objetivo de comprobar que
la respuesta del mismo en distintos escenarios coincida con el resultado esperado.
*/

//* Ejercicio 3
// ?¿Qué es un Test de Integración?
/*
	Son los tests que prueban la comunicacion entre distintos componentes de la aplicacion.
	el objetivo es probar que todo los bloques que se probaron de forma unitaria, interactuen entre si
	y generen los resultados esperados.
*/

